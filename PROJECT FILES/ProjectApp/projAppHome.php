<h1> About </h1>

<p>
  The Arts Projects program financially supports a variety of high quality arts projects
  throughout Tacoma. This funding can be used for artistic presentations, activities,
  workshops or projects that are accessible to the general public. Admission may be
  charged to these programs, as long as the application demonstrates public benefit (see
  definition). The arts activity may be a one-time event or a small number of events that
  are closely related. It may also be an ongoing program or neighborhood public art
  project. Additional consideration will be given to projects that include other funding
  from individuals, businesses, or organizations, and that pay professional artists for their
  professional services. </p>

<p>
  Funding for Arts Projects is determined through a competitive application process.
  Funding from year to year is not guaranteed. Contract awards will be based on budgetary
  factors and the evaluation criteria detailed in these guidelines. Of these, Artistic Quality
  and Artistic Merit are the most important factors in determining the competitiveness of
  an application. </p>


